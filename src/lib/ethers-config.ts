async function getContract() {
    // Connect to Remix VM (Cancun) via JSON-RPC
    const provider = new ethers.JsonRpcProvider('http://0.0.0.0:8545')

    // Get the first available account in Remix VM
    const signer = await provider.getSigner()

    // Connect to the deployed contract
    const contract = new ethers.Contract(CONTRACT_ADDRESS, CONTRACT_ABI, signer) as Contract

    return contract
}

async function getWalletAddress(): Promise<string | null> {
    if (typeof window !== 'undefined' && window.ethereum) {
        const provider = new ethers.BrowserProvider(window.ethereum)
        const account: string = await provider.send('eth_requestAccounts', [])
        return account
    } else {
        console.error('MetaMask is not installed')
        return null
    }
}

